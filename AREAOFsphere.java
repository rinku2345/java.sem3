import java.util.Scanner;
public class AREAOFsphere {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Radius of sphere is =");
        int r=sc.nextInt();
        double pie=3.142;
        System.out.println("AREA OF SPHERE IS ="+4*pie*r*r);

    }
}
